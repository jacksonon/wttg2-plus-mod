		public static int GetWindowsKeyCode(Event ev)
		{
			int keyCode = ev.keyCode;
			if (keyCode == 0)
			{
				if (ev.character == '\n')
				{
					return 13;
				}
				return (int)ev.character;
			}
			else
			{
				if (keyCode >= 97 && keyCode <= 122)
				{
					return keyCode - 97 + 65;
				}
				int result;
				if (KeyMappings.mappings.TryGetValue(ev.keyCode, out result))
				{
					return result;
				}
				Debug.LogWarning("Unknown key mapping: " + ev);
				return keyCode;
			}
		}

